show_sprite proc
	pusha
	mov bx, offset player
	mov ax, 0
	
	mov [y], 1
	mov [x], 1
	
	sprite_loop:
		cmp [y], 125
		jg end_sprite_loop
		
		push bx
		cmp [bx], 0
		je black_pixel
		
		print_pixel:
			mov al, [bx]
			mov bh, 0
			mov cx, [x]
			mov dx, [y]
			mov ah, 0ch
			int 10h
		
		black_pixel:
		pop bx
		cmp [x], 64
		je new_pixel_line
		
		inc [x]
		inc bx
		jmp sprite_loop
		
		new_pixel_line:
		inc [y]
		sub [x], 63
		inc bx
		jmp sprite_loop
		
	end_sprite_loop:
popa
ret
show_sprite endp

